/**
 * Лёгкая типографика для русского текста.
 * Расставляет неразрывные пробелы (&nbsp;) по основным правилам и возвращает
 * строку с HTML-сущностями — поэтому выводить результат нужно ТОЛЬКО через set:html.
 *
 *   import typograf from '@utils/typograf';
 *   <p set:html={typograf(text)} />
 */

const NBSP = '&nbsp;';

// 3-буквенные предлоги/союзы, которые нельзя отрывать от следующего слова
const SHORT3 = 'для|без|под|над|при|про|изо|обо|что|как|или|чем|где|так|это|эта|эти|тот';

export function typograf(input) {
  if (input == null) return '';
  let s = String(input);

  // 1) Числа: разряды (5 900 -> 5&nbsp;900), несколько групп подряд
  for (let i = 0; i < 3; i++) {
    s = s.replace(/(\d)[ \t]+(?=\d{3}\b)/g, `$1${NBSP}`);
  }

  // 2) Число + единица измерения / валюта
  s = s.replace(/(\d)[ \t]+(₽|руб\.?|%|°|мл|г|кг|шт\.?|мм|см)/gi, `$1${NBSP}$2`);

  // 3) Буквенный префикс + число (SPF 15, № 5)
  s = s.replace(/\b(SPF|№)[ \t]+(?=\d)/gi, `$1${NBSP}`);

  // 4) Знаки © и № + следующее слово/число
  s = s.replace(/([©№])[ \t]+/g, `$1${NBSP}`);

  // 5) Тире (— или –) — неразрывный пробел перед тире, сам символ приводим к длинному тире
  s = s.replace(/[ \t]+[—–][ \t]+/g, `${NBSP}— `);

  // 6) Короткие слова (1–2 буквы) — привязываем к следующему слову
  s = s.replace(/(?<=(?:^|[\s(«"„])[A-Za-zА-Яа-яЁё]{1,2})[ \t]+/g, NBSP);

  // 7) 3-буквенные предлоги/союзы — тоже привязываем к следующему слову
  s = s.replace(new RegExp(`(?<=(?:^|[\\s(«"„])(?:${SHORT3}))[ \\t]+`, 'gi'), NBSP);

  // 8) Частицы (бы, же, ли, ль) — привязываем к предыдущему слову
  s = s.replace(/[ \t]+(бы|же|ли|ль)(?=[\s.,!?:;)»]|$)/gi, `${NBSP}$1`);

  return s;
}

export default typograf;
